package week1.day2;

public class Calculator {

	int add(int num1, int num2, int num3) {
		return num1 + num2 + num3;
	}

	int sub(int num1, int num2) {

		return num1 - num2;
	}

	double multiply(double num1, double num2) {

		return num1 * num2;
	}

	float divide(float num1, float num2) {
		return num1 / num2;
	}
}
